/*перетворення у рядок */
/*явне перетворення до рядка 
все що вводиться, воно просто огортається в лапки і виходить вже , як текст*/
console.log (String(10)); //10
console.log (String(true));//true

/*НЕ явне перетворення до рядка 
якщо додавати щось до рядка, то те що додається - стане рядком */
